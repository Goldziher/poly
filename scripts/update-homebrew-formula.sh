#!/usr/bin/env bash
set -euo pipefail

# update-homebrew-formula.sh <version> <checksums-file>
#
# Emits a Homebrew formula (to stdout) for the PREBUILT poly binaries, pulling
# the per-platform archive URLs from the GitHub release and the sha256 digests
# from the release checksums file (`sha256sums.txt`, as produced by the
# `checksums` job in .github/workflows/publish.yaml). The release workflow runs
# this for stable releases and pushes the result to Goldziher/homebrew-tap.
#
# Each archive holds the statically-linked `poly` binary at the root, so the
# formula installs it onto PATH.
# Windows (.zip) is not a Homebrew target; only the four macOS/Linux gnu
# triples are emitted.

if [ $# -ne 2 ]; then
  echo "Usage: $0 <version> <checksums-file>" >&2
  exit 1
fi

VERSION="$1"
SUMS="$2"
BASE="https://github.com/Goldziher/polylint/releases/download/v${VERSION}"

# Resolve the sha256 for a given target triple. sha256sums.txt lines look like
# "<sha>  ./poly-<version>-<triple>.tar.gz" (the `./` comes from sha256sum being
# run inside the artifacts dir); strip an optional leading "./" or "*" marker.
sha_for() {
  awk -v f="poly-${VERSION}-$1.tar.gz" '
    { n = $2; sub(/^\.\//, "", n); sub(/^\*/, "", n); if (n == f) print $1 }
  ' "$SUMS"
}

MAC_ARM=$(sha_for aarch64-apple-darwin)
MAC_X64=$(sha_for x86_64-apple-darwin)
LINUX_ARM=$(sha_for aarch64-unknown-linux-gnu)
LINUX_X64=$(sha_for x86_64-unknown-linux-gnu)

for pair in "aarch64-apple-darwin:$MAC_ARM" "x86_64-apple-darwin:$MAC_X64" \
  "aarch64-unknown-linux-gnu:$LINUX_ARM" "x86_64-unknown-linux-gnu:$LINUX_X64"; do
  if [ -z "${pair#*:}" ]; then
    echo "missing checksum for ${pair%%:*} in $SUMS" >&2
    exit 1
  fi
done

cat <<EOF
# typed: false
# frozen_string_literal: true

# Generated by scripts/update-homebrew-formula.sh — do not edit by hand.
class Polylint < Formula
  desc "Universal zero-dependency linter & formatter"
  homepage "https://github.com/Goldziher/polylint"
  version "${VERSION}"
  license "MIT"

  on_macos do
    on_arm do
      url "${BASE}/poly-${VERSION}-aarch64-apple-darwin.tar.gz"
      sha256 "${MAC_ARM}"
    end
    on_intel do
      url "${BASE}/poly-${VERSION}-x86_64-apple-darwin.tar.gz"
      sha256 "${MAC_X64}"
    end
  end

  on_linux do
    on_arm do
      url "${BASE}/poly-${VERSION}-aarch64-unknown-linux-gnu.tar.gz"
      sha256 "${LINUX_ARM}"
    end
    on_intel do
      url "${BASE}/poly-${VERSION}-x86_64-unknown-linux-gnu.tar.gz"
      sha256 "${LINUX_X64}"
    end
  end

  def install
    bin.install "poly"
  end

  test do
    assert_match version.to_s, shell_output("#{bin}/poly --version")
  end
end
EOF
